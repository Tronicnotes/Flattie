import SwiftUI

extension Color {

    // MARK: - Background Colours
    static let backgroundPrimary: Color = .init("TealLightest")

    // MARK: - Card Shadow
    static let cardShadow: Color = .init("BlueGrey")

    // MARK: - Primary Colours
    static let primary: Color = .init("TealPrimary")
    static let primaryLight: Color = .init("TealLight")
    static let secondary: Color = .init("TealLighter")

    // MARK: - Text Colours
    static let textDark: Color = .init("Navy")
    static let textLight: Color = .init("TealLightest")
    static let textSecondary: Color = .init("NavyLight")
    static let textGrey: Color = .init("BlueGrey")
    static let textGreyLight: Color = .init("BlueGreyLight")

    // MARK: - Item Colours
    static let itemBlue: Color = .init("PastelBlue")
    static let itemBrown: Color = .init("PastelBrown")
    static let itemGrey: Color = .init("PastelGrey")
    static let itemPeach: Color = .init("PastelPeach")
    static let itemPurple: Color = .init("PastelPurple")
    static let itemRed: Color = .init("PastelRed")
    static let itemTeal: Color = .init("PastelTeal")
    static let itemYellow: Color = .init("PastelYellow")
    static let itemNavy: Color = .init("Navy")
}
